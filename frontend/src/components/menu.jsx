import React, { useState, useEffect } from 'react';
import { Link } from 'react-router-dom';
import '../styles/Menu.scss';
import '../styles/LeftMenu.scss';

function Menu(props) {
  const [profilePic, setProfilePic] = useState('');
  const [expanded, setExpanded] = useState(false);
  const [notifications, setNotifications] = useState([]);
  const [nightMode, setNightMode] = useState(false); // Add night mode state

  async function fetchData() {
    const res = await fetch(`http://127.0.0.1:8001/users/${sessionStorage.getItem("userid")}`);
    const data = await res.json();
    setProfilePic(`http://127.0.0.1:8001/${data.picture}`);
  }

  // Fetch night mode setting
  async function fetchNightmode() {
    try {
      const res = await fetch(`http://127.0.0.1:8001/users/${sessionStorage.getItem("userid")}`);
      const data = await res.json();
      setNightMode(data.nightmode);

      const r = document.querySelector(':root');
      r.style.setProperty("--bg-color", data.nightmode ? "#333" : "#F8F9FB");
      r.style.setProperty("--bg-color-secondary", data.nightmode ? "#4f4f4f" : "white");
      r.style.setProperty("--bg-color-tertiary", data.nightmode ? "#404040" : "#f3e3e660");
      r.style.setProperty("--text-color", data.nightmode ? "white" : "black");
      r.style.setProperty("--outline-color", data.nightmode ? "#c3ffc2" : "green");
      r.style.setProperty("--fill-color", data.nightmode ? "white" : "gray");
      r.style.setProperty("--btn-color", data.nightmode ? "rgba(195, 255, 194, 0.082)" : "rgba(0, 128, 0, 0.082)");
    } catch (error) {
      console.error("Error fetching night mode setting:", error);
    }
  };

  useEffect(() => {
    // Fetch profile picture
    async function fetchProfile() {
      await fetchData();
    }

    fetchProfile();
    fetchNightmode();
  }, []);

  function handleSearch(e) {
    if (props.handleSearch != undefined) {
      props.handleSearch(e);
    }
  }

  async function toggleNightmode() {
    try {
      const body = {
        "userid": sessionStorage.getItem("userid"),
      };

      const response = await fetch("http://127.0.0.1:8001/users/toggle-night-mode", {
        method: "POST",
        body: JSON.stringify(body),
        headers: {
          "Content-type": "application/json"
        }
      });

      if (response.ok) {
        await fetchNightmode();
      } else {
        console.error("Failed to toggle night mode");
      }
    } catch (error) {
      console.error("Error toggling night mode:", error);
    }
  }

  return (
    <>
      <div className="menu">
        <div className="menu-left">
          <svg xmlns="http://www.w3.org/2000/svg" width="32" height="32" fill="green" className="bi bi-list" viewBox="0 0 16 16" onClick={() => setExpanded(!expanded)}>
            <path fillRule="evenodd" d="M2.5 12a.5.5 0 0 1 .5-.5h10a.5.5 0 0 1 0 1H3a.5.5 0 0 1-.5-.5m0-4a.5.5 0 0 1 .5-.5h10a.5.5 0 0 1 0 1H3a.5.5 0 0 1-.5-.5m0-4a.5.5 0 0 1 .5-.5h10a.5.5 0 0 1 0 1H3a.5.5 0 0 1-.5-.5" />
          </svg>
          <input type="text" placeholder="Search course name" onChange={handleSearch} />
        </div>

        <div className="icons">
          <a>
            {
              !nightMode &&
              <svg xmlns="http://www.w3.org/2000/svg" width="24" height="24" fill="currentColor" className="bi bi-moon" viewBox="0 0 16 16" onClick={toggleNightmode}>
                <path d="M6 .278a.77.77 0 0 1 .08.858 7.2 7.2 0 0 0-.878 3.46c0 4.021 3.278 7.277 7.318 7.277q.792-.001 1.533-.16a.79.79 0 0 1 .81.316.73.73 0 0 1-.031.893A8.35 8.35 0 0 1 8.344 16C3.734 16 0 12.286 0 7.71 0 4.266 2.114 1.312 5.124.06A.75.75 0 0 1 6 .278M4.858 1.311A7.27 7.27 0 0 0 1.025 7.71c0 4.02 3.279 7.276 7.319 7.276a7.32 7.32 0 0 0 5.205-2.162q-.506.063-1.029.063c-4.61 0-8.343-3.714-8.343-8.29 0-1.167.242-2.278.681-3.286" />
              </svg>
            }
            {
              nightMode &&
              <svg xmlns="http://www.w3.org/2000/svg" width="24" height="24" fill="currentColor" class="bi bi-brightness-high" viewBox="0 0 16 16" onClick={toggleNightmode}>
                <path d="M8 11a3 3 0 1 1 0-6 3 3 0 0 1 0 6m0 1a4 4 0 1 0 0-8 4 4 0 0 0 0 8M8 0a.5.5 0 0 1 .5.5v2a.5.5 0 0 1-1 0v-2A.5.5 0 0 1 8 0m0 13a.5.5 0 0 1 .5.5v2a.5.5 0 0 1-1 0v-2A.5.5 0 0 1 8 13m8-5a.5.5 0 0 1-.5.5h-2a.5.5 0 0 1 0-1h2a.5.5 0 0 1 .5.5M3 8a.5.5 0 0 1-.5.5h-2a.5.5 0 0 1 0-1h2A.5.5 0 0 1 3 8m10.657-5.657a.5.5 0 0 1 0 .707l-1.414 1.415a.5.5 0 1 1-.707-.708l1.414-1.414a.5.5 0 0 1 .707 0m-9.193 9.193a.5.5 0 0 1 0 .707L3.05 13.657a.5.5 0 0 1-.707-.707l1.414-1.414a.5.5 0 0 1 .707 0m9.193 2.121a.5.5 0 0 1-.707 0l-1.414-1.414a.5.5 0 0 1 .707-.707l1.414 1.414a.5.5 0 0 1 0 .707M4.464 4.465a.5.5 0 0 1-.707 0L2.343 3.05a.5.5 0 1 1 .707-.707l1.414 1.414a.5.5 0 0 1 0 .708" />
              </svg>
            }
          </a>

          <Link to="/notifications">
            <svg xmlns="http://www.w3.org/2000/svg" width="24" height="24" fill="gray" className="bi bi-bell" viewBox="0 0 16 16">
              <path d="M8 16a2 2 0 0 0 2-2H6a2 2 0 0 0 2 2M8 1.918l-.797.161A4 4 0 0 0 4 6c0 .628-.134 2.197-.459 3.742-.16.767-.376 1.566-.663 2.258h10.244c-.287-.692-.502-1.49-.663-2.258C12.134 8.197 12 6.628 12 6a4 4 0 0 0-3.203-3.92zM14.22 12c.223.447.481.801.78 1H1c.299-.199.557-.553.78-1C2.68 10.2 3 6.88 3 6c0-2.42 1.72-4.44 4.005-4.901a1 1 0 1 1 1.99 0A5 5 0 0 1 13 6c0 .88.32 4.2 1.22 6" />
            </svg>
            {Array.isArray(notifications) && notifications.length > 0 && <span>{notifications.length}</span>}
          </Link>

          <Link to="/messages">
            <svg xmlns="http://www.w3.org/2000/svg" width="24" height="24" fill="gray" className="bi bi-envelope" viewBox="0 0 16 16">
              <path d="M0 4a2 2 0 0 1 2-2h12a2 2 0 0 1 2 2v8a2 2 0 0 1-2 2H2a2 2 0 0 1-2-2zm2-1a1 1 0 0 0-1 1v.217l7 4.2 7-4.2V4a1 1 0 0 0-1-1zm13 2.383-4.708 2.825L15 11.105zm-.034 6.876-5.64-3.471L8 9.583l-1.326-.795-5.64 3.47A1 1 0 0 0 2 13h12a1 1 0 0 0 .966-.741M1 11.105l4.708-2.897L1 5.383z" />
            </svg>
          </Link>

          <Link to="/profile">
            {profilePic ?
              <img src={profilePic} alt="Profile" /> :
              <svg>{/* Profile SVG icon */}</svg>}
          </Link>
        </div>
      </div>

      <div className={`left-menu ${expanded ? "expanded" : ""}`}>
        {/* Dashboard */}
        <Link to="/dashboard" className="menu-item">
          <svg xmlns="http://www.w3.org/2000/svg" width="32" height="32" fill="gray" className="bi bi-grid" viewBox="0 0 16 16">
            <path d="M1 2.5A1.5 1.5 0 0 1 2.5 1h3A1.5 1.5 0 0 1 7 2.5v3A1.5 1.5 0 0 1 5.5 7h-3A1.5 1.5 0 0 1 1 5.5zM2.5 2a.5.5 0 0 0-.5.5v3a.5.5 0 0 0 .5.5h3a.5.5 0 0 0 .5-.5v-3a.5.5 0 0 0-.5-.5zm6.5.5A1.5 1.5 0 0 1 10.5 1h3A1.5 1.5 0 0 1 15 2.5v3A1.5 1.5 0 0 1 13.5 7h-3A1.5 1.5 0 0 1 9 5.5zm1.5-.5a.5.5 0 0 0-.5.5v3a.5.5 0 0 0 .5.5h3a.5.5 0 0 0 .5-.5v-3a.5.5 0 0 0-.5-.5zM1 10.5A1.5 1.5 0 0 1 2.5 9h3A1.5 1.5 0 0 1 7 10.5v3A1.5 1.5 0 0 1 5.5 15h-3A1.5 1.5 0 0 1 1 13.5zm1.5-.5a.5.5 0 0 0-.5.5v3a.5.5 0 0 0 .5.5h3a.5.5 0 0 0 .5-.5v-3a.5.5 0 0 0-.5-.5zm6.5.5A1.5 1.5 0 0 1 10.5 9h3a1.5 1.5 0 0 1 1.5 1.5v3a1.5 1.5 0 0 1-1.5 1.5h-3A1.5 1.5 0 0 1 9 13.5zm1.5-.5a.5.5 0 0 0-.5.5v3a.5.5 0 0 0 .5.5h3a.5.5 0 0 0 .5-.5v-3a.5.5 0 0 0-.5-.5z" />
          </svg>

          {expanded && <span>Dashboard</span>}
        </Link>

        {/* Messages */}
        <Link to="/messages" className="menu-item">
          <svg xmlns="http://www.w3.org/2000/svg" width="32" height="32" fill="gray" className="bi bi-chat-left-dots" viewBox="0 0 16 16">
            <path d="M14 1a1 1 0 0 1 1 1v8a1 1 0 0 1-1 1H4.414A2 2 0 0 0 3 11.586l-2 2V2a1 1 0 0 1 1-1zM2 0a2 2 0 0 0-2 2v12.793a.5.5 0 0 0 .854.353l2.853-2.853A1 1 0 0 1 4.414 12H14a2 2 0 0 0 2-2V2a2 2 0 0 0-2-2z" />
            <path d="M5 6a1 1 0 1 1-2 0 1 1 0 0 1 2 0m4 0a1 1 0 1 1-2 0 1 1 0 0 1 2 0m4 0a1 1 0 1 1-2 0 1 1 0 0 1 2 0" />
          </svg>

          {expanded && <span>Messages</span>}
        </Link>

        {/* Profile Settings */}
        <Link to="/profile" className="menu-item">
          <svg xmlns="http://www.w3.org/2000/svg" width="32" height="32" fill="gray" className="bi bi-gear" viewBox="0 0 16 16">
            <path d="M8 4.754a3.246 3.246 0 1 0 0 6.492 3.246 3.246 0 0 0 0-6.492M5.754 8a2.246 2.246 0 1 1 4.492 0 2.246 2.246 0 0 1-4.492 0" />
            <path d="M9.796 1.343c-.527-1.79-3.065-1.79-3.592 0l-.094.319a.873.873 0 0 1-1.255.52l-.292-.16c-1.64-.892-3.433.902-2.54 2.541l.159.292a.873.873 0 0 1-.52 1.255l-.319.094c-1.79.527-1.79 3.065 0 3.592l.319.094a.873.873 0 0 1 .52 1.255l-.16.292c-.892 1.64.901 3.434 2.541 2.54l.292-.159a.873.873 0 0 1 1.255.52l.094.319c.527 1.79 3.065 1.79 3.592 0l.094-.319a.873.873 0 0 1 1.255-.52l.292.16c1.64.893 3.434-.902 2.54-2.541l-.159-.292a.873.873 0 0 1 .52-1.255l.319-.094c1.79-.527 1.79-3.065 0-3.592l-.319-.094a.873.873 0 0 1-.52-1.255l.16-.292c.893-1.64-.902-3.433-2.541-2.54l-.292.159a.873.873 0 0 1-1.255-.52zm-2.633.283c.246-.835 1.428-.835 1.674 0l.094.319a1.873 1.873 0 0 0 2.693 1.115l.291-.16c.764-.415 1.6.42 1.184 1.185l-.159.292a1.873 1.873 0 0 0 1.116 2.692l.318.094c.835.246.835 1.428 0 1.674l-.319.094a1.873 1.873 0 0 0-1.115 2.693l.16.291c.415.764-.42 1.6-1.185 1.184l-.291-.159a1.873 1.873 0 0 0-2.693 1.116l-.094.318c-.246.835-1.428.835-1.674 0l-.094-.319a1.873 1.873 0 0 0-2.692-1.115l-.292.16c-.764.415-1.6-.42-1.184-1.185l.159-.291A1.873 1.873 0 0 0 1.945 8.93l-.319-.094c-.835-.246-.835-1.428 0-1.674l.319-.094A1.873 1.873 0 0 0 3.06 4.377l-.16-.292c-.415-.764.42-1.6 1.185-1.184l.292.159a1.873 1.873 0 0 0 2.692-1.115z" />
          </svg>

          {expanded && <span>Profile Settings</span>}
        </Link>
      </div>
    </>
  );
}

export default Menu;
